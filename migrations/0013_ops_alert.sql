-- 0013: стан операційних алертів (перший — «збір зупинився»).
--
-- Навіщо таблиця, а не файл на диску: без пам'яті сторож або мовчить (і ти не знаєш
-- про падіння), або дзвонить щочверть години, поки воно триває. Тут же зберігається
-- ПЕРЕХІД стану, тож повідомлення йде один раз на подію, а не на кожну перевірку.
--
-- Другий, не менш важливий сенс — ІСТОРІЯ. Після сьогоднішньої двогодинної зупинки
-- (2026-07-21) не було як відповісти навіть на просте «а часто таке буває». Тепер
-- буде: changed_at показує, коли стан змінився, sent_count — скільки разів нагадували.
--
-- Ключ — name, тож наступні сторожі (місце на диску, протухла БД) лягають сюди ж
-- без нової таблиці.

CREATE TABLE IF NOT EXISTS ops_alert (
  name         TEXT PRIMARY KEY,
  state        TEXT NOT NULL CHECK (state IN ('ok', 'down')),
  changed_at   TIMESTAMPTZ NOT NULL DEFAULT now(),
  last_sent_at TIMESTAMPTZ,
  sent_count   INTEGER NOT NULL DEFAULT 0,
  note         TEXT
);
