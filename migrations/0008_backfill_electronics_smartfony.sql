-- 0008: одноразовий бекфіл категорії для НАЯВНОЇ електроніки.
--
-- Категорія присвоюється при інджесті (persist_items: ON CONFLICT DO UPDATE SET category_id).
-- Товари, зібрані ДО 0007, лишились у «Інше», бо тегованої-з-лістинга логіки тоді не було —
-- вони перетеглись би лише при наступному зборі того ж лістинга (а хвости, що зникли зі
-- сторінки, не перетеглись би ніколи). Тож пересуваємо їх разово.
--
-- Чому це коректно САМЕ ЗАРАЗ (Фаза 1): усі 8 джерел електроніки тягнуть ЛИШЕ смартфон-
-- лістинги (див. HTML_SOURCES) → кожен їхній товар у «Інше» = смартфон. Майбутні збори
-- тегують самі (категорія-з-лістинга); нові категорії Фази 2 приходять уже зі своїм тегом,
-- тож ця разова міграція не конфліктує з ними (WHERE category_id = «Інше», forward-only).
--
-- Ідемпотентно: після переходу товар більше не в «Інше» → повторний запуск нічого не робить.

UPDATE store_product sp
SET category_id = (SELECT category_id FROM category WHERE slug = 'smartfony')
FROM source s
WHERE sp.source_id = s.source_id
  AND s.name IN ('Allo', 'Foxtrot', 'Moyo', 'Comfy', 'Rozetka', 'Citrus', 'Brain', 'KTC')
  AND sp.category_id = (SELECT category_id FROM category WHERE slug = 'inshe');
