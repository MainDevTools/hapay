-- 0004: крос-крамничний матчинг за MPN (T15, §17.5).
-- mpn — заводський артикул із назви товару (matching.extract_mpn, СЕРВЕРНА екстракція
-- при персисті). Однаковий mpn у різних крамницях = одна агрегатна картка «Де купити».
-- NULL — товар без артикула (зоотовари тощо): просто не групується, поведінка як досі.
-- Бекфіл не потрібен: колектор ходить 2×/добу і upsert-ом заповнить існуючі рядки.

ALTER TABLE store_product ADD COLUMN mpn TEXT;

-- частковий індекс: шукаємо лише товари З артикулом (NULL-ів більшість — зоо)
CREATE INDEX ix_sp_mpn ON store_product (mpn) WHERE mpn IS NOT NULL;
