-- 0012: нижній поріг заявленої знижки — 1%.
--
-- Рішення власника (2026-07-21) на пряме питання: «чи вважати знижкою 0,01%».
-- Пороги детекції — під людським рев'ю (інваріант C), тож фіксуємо і рішення, і причину.
--
-- ЩО СТАЛОСЬ. Нове сортування «Де дешевше» підняло нагору картку 89 999 ₴ проти
-- 90 199 ₴ — «знижка» 200 ₴, тобто 0,22%, яку показ округляв у червоний бейдж «−0%».
-- Але це був лише симптом: подія знижки заводилась на БУДЬ-яке зниження ціни, тож
-- у стрічку «зі знижками» такі товари потрапляли нарівні зі справжніми −47%.
--
-- ЧОМУ ПОРІГ ДО ОКРУГЛЕННЯ. Заміряно на проді: подій нижче 1% було 27 — з них лише 7
-- малювали «−0%», а 20 округлялись до «−1%» і виглядали як справжня знижка. Якби поріг
-- порівнював округлене значення, ці 20 пролізли б. Найменша була 0,011%.
--
-- Поріг у detection_config (а не в коді) — щоб власник міняв його без деплою, як решту.

ALTER TABLE detection_config
  ADD COLUMN IF NOT EXISTS min_declared_pct INTEGER NOT NULL DEFAULT 1;

-- Закриваємо вже відкриті події нижче порога. Без цього вони жили б вічно: detect_pass
-- бачить відкриту подію (frozen announce) і продовжує її оновлювати, навіть коли нова
-- вже не завелася б.
--
-- verified/verified_provisional НЕ чіпаємо: там знижка інферована з ІСТОРІЇ цін, а не
-- зі слів крамниці, і мала заявлена дельта її не спростовує. На момент міграції таких
-- серед підпорогових не було (0 із 27), але правило має бути в SQL, а не в припущенні.
UPDATE discount_event SET ended_at = now()
WHERE ended_at IS NULL
  AND badge_state NOT IN ('verified', 'verified_provisional')
  AND old_declared_kop IS NOT NULL
  AND old_declared_kop > current_kop
  AND (old_declared_kop - current_kop) * 100.0 / old_declared_kop
      -- COALESCE обовʼязковий: порожня detection_config дала б NULL, порівняння «< NULL»
      -- це NULL, і міграція ТИХО не зробила б нічого. Мовчазний no-op гірший за помилку.
      < COALESCE((SELECT min_declared_pct FROM detection_config
                  WHERE valid_from <= now() AND (valid_to IS NULL OR valid_to > now())
                  ORDER BY valid_from DESC LIMIT 1), 1);
