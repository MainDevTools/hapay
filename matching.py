"""Крос-крамничний матчинг товарів (§17.5). Два ключі, спільний інваріант.

Той самий фізичний товар у різних крамницях треба звести в одну картку «Де купити».
Ключ залежить від домену:

- **MPN** (T15) — заводський артикул із назви: «Samsung Galaxy A37 (SM-A376BDGGEUC)».
  Єдиний доступний ключ для електроніки, бо штрихкодів крамниці не публікують (T17).
- **GTIN** (2026-07-22) — штрихкод EAN/UPC із розмітки: аптеки/медтовари віддають його
  для кожного товару. Сильніший за MPN — глобальний код на коробці, не здогад про назву.

Спільний принцип обох: НЕДОзлиття безпечне (дві картки замість одної), ПЕРЕзлиття —
брехня (чужі ціни на картці). Тому правила навмисно суворі — див. коментарі нижче
біля кожного ключа. Пріоритет при персисті: GTIN, якщо є валідний; інакше MPN.
"""
from __future__ import annotations

import re

# Samsung: артикул упізнаваний і поза дужками (наскрізний ключ розвідки 2026-07-18)
_SAMSUNG = re.compile(r"\bSM-[A-Z0-9]{4,15}\b")

# родовий кандидат: останній токен У ДУЖКАХ з латиниці/цифр (Apple MG6J4, MHRV4AF/A …).
# Крапка дозволена — Acer пише «(NX.EL5EU.003)», без неї цей артикул не ловився зовсім.
_PAREN = re.compile(r"\(([A-Z0-9][A-Z0-9/\-.]{3,24})\)")

# Артикул ІНЛАЙН, без дужок: так пишуть телевізори («LG 43UA75006LA»,
# «SAMSUNG QE50Q7FAAUXUA», «HAIER H50S80FUX») і частина ноутбуків («NX.DK6EU.008»).
# Без цього правила ТВ не зіставлялись узагалі (розвідка 2026-07-20: той самий
# LG 43UA75006LA лежав 4 окремими картками в 4 крамницях).
# Ризик перезлиття тут вищий, ніж у дужках, тому вимоги СУВОРІШІ (див. _plausible_inline):
# лише ВЕЛИКІ літери, ≥8 символів, ≥2 літери І ≥2 цифри.
_INLINE = re.compile(r"\b[A-Z0-9]+(?:[.\-/][A-Z0-9]+)*\b")
_INLINE_MIN = 8

# сміття, що схоже на артикул формою, але ним не є
_STOP = {"NEW", "SALE", "LTE", "NFC", "USB", "OLED", "AMOLED", "IPS",
         "HD", "FHD", "UHD", "QHD", "WIFI", "5G", "4G", "3G", "2G"}
# памʼять/характеристики: «256GB», «8/256GB», «6000MAH», «120HZ», «55W»
_SPEC = re.compile(r"[\d/]+(GB|TB|MAH|MM|CM|HZ|W|K)?")


def _plausible(tok: str) -> bool:
    """Форм-фільтр родового кандидата: ≥2 літери, ≥1 цифра, не спека, не стоп-слово."""
    if not (4 <= len(tok) <= 25) or tok in _STOP:
        return False
    letters = sum(c.isalpha() for c in tok)
    digits = sum(c.isdigit() for c in tok)
    if letters < 2 or digits < 1:
        return False          # «(PRO)» — без цифр; «(2025)» — без літер; EAN — самі цифри
    if _SPEC.fullmatch(tok):
        return False          # «256GB», «8/256GB», «6000MAH» — характеристика, не артикул
    return True


def _plausible_inline(tok: str) -> bool:
    """Форм-фільтр ІНЛАЙН-кандидата — суворіший за дужковий, бо поза дужками навколо
    багато сміття (роздільна здатність, діагональ, маркетинг).

    Довжина ≥8 навмисна: відсікає серію без конфігурації («65B5», «X1504VA»), яка
    СПІЛЬНА для різних комплектацій → саме на ній стається перезлиття (пастка AUXUA).
    Вимога ≥2 цифр і ≥2 літер відсікає «3840X2160», «SAMSUNG», «QLED», «WI-FI».
    """
    if not (_INLINE_MIN <= len(tok) <= 25) or tok in _STOP:
        return False
    if tok.startswith("SM-"):
        return False          # Samsung має власне правило (≥11); інлайн не сміє
    # брати коротку модель — вона спільна для варіантів памʼяті/кольору (перезлиття)
    letters = sum(c.isalpha() for c in tok)
    digits = sum(c.isdigit() for c in tok)
    if letters < 2 or digits < 2:
        return False
    if _SPEC.fullmatch(tok):
        return False
    return True


# ── GTIN / штрихкод: глобальний ідентифікатор товару (аптеки/медтовари, 2026-07-22) ──
# Матчинг за назвою для електроніки впирався в стелю (T17): у товарів немає штрихкодів
# ані в лістингах, ані на сторінках, тож ключем лишався тендітний артикул із назви. В
# аптечному домені навпаки — Подорожник віддає масив `gtins` для КОЖНОГО товару.
# GTIN (EAN-8 / UPC-12 / EAN-13 / GTIN-14) — глобально унікальний код на коробці, тож
# збіг за ним = той самий фізичний товар ЗА ПОБУДОВОЮ, а не за здогадом про назву.
#
# Той самий інваріант, що й у MPN (T15): НЕДОзлиття безпечне, ПЕРЕзлиття — брехня на
# картці. GTIN сильніший за назву, але має два власні способи збрехати, і обидва тут
# закриті:
#   1. Бита контрольна цифра. Останній розряд GTIN — контрольна сума решти; зіпсований
#      чи недодрукований штрихкод її не пройде, тож не створить хибної групи.
#   2. Коди ОБМЕЖЕНОГО ОБІГУ (внутрішньомагазинні, ваговий товар): EAN-13/UPC із першою
#      цифрою «2», EAN-8 із «0»/«2». Вони НЕ глобальні — той самий код у двох крамницях
#      означає РІЗНІ товари. Прийняти такий = гарантоване перезлиття, тому відкидаємо.
#
# Канон — 14 цифр із ведучими нулями (GTIN-14), щоб UPC-12 і його EAN-13 (той самий
# товар, різна довжина запису) зводились до одного ключа.


def _gtin_check_ok(digits: str) -> bool:
    """Контрольна цифра GTIN (уніфіковано для 8/12/13/14): справа наліво ×3,1,3,1…"""
    body, check = digits[:-1], digits[-1]
    s = sum(int(d) * (3 if i % 2 == 0 else 1) for i, d in enumerate(reversed(body)))
    return (10 - s % 10) % 10 == int(check)


def _gtin_restricted(digits: str) -> bool:
    """Код обмеженого обігу (внутрішньомагазинний) — не глобальний, ключем бути не може."""
    n = len(digits)
    if n in (12, 13):
        return digits[0] == "2"
    if n == 8:
        return digits[0] in "02"
    if n == 14:                       # GTIN-14 = індикатор пакування + EAN-13
        return digits[1] == "2"
    return False


def normalize_gtin(raw) -> str | None:
    """Штрихкод → канонічний GTIN-14 (рядок із 14 цифр) або None.

    None = «непридатний як ключ»: не число, чужа довжина, бита контрольна цифра або
    код обмеженого обігу. Приймаємо лише те, що напевно є глобальним ідентифікатором;
    решту чесніше лишити без ключа (недозлиття), ніж ризикнути перезлиттям.
    """
    if raw is None:
        return None
    d = re.sub(r"\D", "", str(raw))
    if len(d) not in (8, 12, 13, 14):
        return None
    if not _gtin_check_ok(d) or _gtin_restricted(d):
        return None
    return d.zfill(14)


def pick_gtin(gtins) -> str | None:
    """Канонічний GTIN товару для запису в ключ, або None.

    Товар може мати кілька штрихкодів (Подорожник віддає різні партії/виробників однієї
    полиці одним записом). Беремо МІНІМАЛЬНИЙ валідний — детермінований вибір, тож
    однакові набори в різних крамницях дають однаковий ключ і сходяться в групу. Якщо
    набори лише ПЕРЕТИНАЮТЬСЯ, але різняться, отримаємо недозлиття — безпечний бік
    інваріанта; повне зіставлення за перетином множин — можливе розширення, коли замір
    покаже, що хвіст вартий того.
    """
    valid = {n for g in (gtins or ()) if (n := normalize_gtin(g))}
    return min(valid) if valid else None


def extract_mpn(title: str | None) -> str | None:
    """MPN з назви товару або None. Детермінований, без нормалізації суфіксів."""
    if not title:
        return None
    # Samsung: беремо НАЙДОВШИЙ збіг — Foxtrot пише «SM-S942B Galaxy … (SM-S942BZKGEUC)»:
    # коротка модель інлайн СПІЛЬНА для всіх варіантів памʼяті/кольору → ключем бути не може
    # (перезлиття), повний артикул у дужках — може. Короткий (<11) без повного → None.
    cands = _SAMSUNG.findall(title)
    if cands:
        best = max(cands, key=len)
        if len(best) >= 11:
            return best
    # родовий: беремо ОСТАННІЙ правдоподібний токен у дужках (артикул зазвичай наприкінці)
    for tok in reversed(_PAREN.findall(title)):
        if _plausible(tok):
            return tok
    # інлайн — ЛИШЕ якщо дужкового немає (дужковий надійніший). Беремо ОСТАННІЙ,
    # як і в дужках: заводський артикул стоїть у кінці, перед ним — модельне позначення
    # («Acer Aspire Lite AL16-54P-56ES NX.DK6EU.008»). Вимірено на 371 назві: кандидатів
    # кілька лише в 7, і в усіх 7 правильний саме останній. Бонус — інші крамниці пишуть
    # цей самий заводський код У ДУЖКАХ, тож ключі сходяться між правилами.
    inline = [t for t in _INLINE.findall(title) if _plausible_inline(t)]
    if inline:
        return inline[-1]
    return None
