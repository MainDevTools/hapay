_AMMO = ("oshejnik", "oshejniki", "amunic", "povid", "nashijnyk", "nashyjnyk", "collar", "leash")


def categorize(url: str) -> str:
    low = (url or "").lower()
    if any(k in low for k in _AMMO):
        return "amunitsiya"
    animal = next((a for a, keys in _ANIMAL.items() if any(k in low for k in keys)), None)
    if "shampun" in low or "shampoo" in low:
        typ = "shampuni"
    elif any(k in low for k in ("konserv", "vologij", "vologi", "vlazh", "vlazhn", "wet-food")):
        typ = "konservy"
    elif any(k in low for k in ("suhoi-korm", "suxoj-korm", "suhij", "suhyi", "suhoy", "dry-food", "suxij")):
        typ = "suhyi-korm"
    else:
        typ = None
    if animal and typ:
        return f"{animal}-{typ}"
    return "inshe"


# ── уточнення категорії за назвою (для пристроєвих лістингів) ──────────────────────
# Пристроєві категорії, у які просочуються аксесуари (широкі департаменти крамниць).
_DEVICE_CATS = {"smartfony", "noutbuky", "planshety", "tv"}

# Сильні сигнали, яких у назві САМОГО пристрою практично не буває (кабель/навушники…).
# Тримаємо консервативно й у синхроні з бекфілом 0009 (ті самі слова як ILIKE).
_AUDIO_RE = re.compile(r"навушник|гарнітур|airpods|earbuds", re.I)
# Гарнітури (навушники з мікрофоном) виділяємо з audio у власну полицю — за назвою, яку
# дала САМА крамниця («Гарнітура …»), тож межа чиста (довіряємо класифікації крамниці),
# а не нашому здогаду. У синхроні з бекфілом 0040. Перевіряється ПЕРЕД audio.
_HEADSET_RE = re.compile(r"гарнітур|headset", re.I)
_ACC_RE = re.compile(
    r"кабель|power\s*bank|павербанк|повербанк|зарядний пристрій|зовнішній акумулятор|"
    r"чохол|захисне скло|захисна плівк|автотримач|стилус", re.I)

# ── розщеплення широкого лістинга «побутова техніка» на власні полиці ──────────────
# Великі прилади (холодильники, пральні, посудомийки) збираються одним широким листингом
# `pobut-tehnika` і варті власної полиці — як pylososy/mikrohvylovky, лише виділяємо за
# назвою, а не окремим збором (товари вже є). Патерни — у СИНХРОНІ з бекфіл-міграціями
# (ті самі слова як ILIKE), спрацьовують на словах, яких у назві іншого приладу не буває.
# Крок за кроком: холодильники (0019). Далі — дописати рядок + категорію + бекфіл.
_APPLIANCE_RE = (
    ("holodylnyky",    re.compile(r"холодильник", re.I)),
    ("pralni-mashyny", re.compile(r"пральн|стиральн", re.I)),
    ("posudomyiky",    re.compile(r"посудомий|посудомоеч", re.I)),
    # сушильні ПІСЛЯ пральних: «прально-сушильна» має лишитись у пральних (комбо), тож
    # порядок у кортежі важливий — пральні перевіряються першими (вище). Слово «сушарка»
    # НЕ беремо: то частіше сушарка для овочів/білизни, не сушильна машина.
    ("sushylni-mashyny", re.compile(r"сушильн", re.I)),
)


def refine_category(base_slug: str, title: str) -> str:
    """Уточнює категорію-з-лістинга за назвою товару. Два широкі лістинги:
    · пристроєві (smartfony/noutbuky/…): кабель/зарядка/чохол → 'aksesuary', навушники → 'audio';
    · 'pobut-tehnika': холодильник → 'holodylnyky' (та наступні прилади).
    Інші бази (зоо, аптека) лишає незмінними.

    Високоточно: спрацьовує тільки на словах, яких у назві іншого товару категорії не
    буває, тож реальні пристрої/прилади не перетасовує.
    """
    t = title or ""
    if base_slug in _DEVICE_CATS:
        if _AUDIO_RE.search(t):
            return "harnitury" if _HEADSET_RE.search(t) else "audio"  # гарнітура ≠ навушники
        if _ACC_RE.search(t):
            return "aksesuary"
        return base_slug
    if base_slug == "audio":                       # навушники-лістинг → гарнітури окремо
        return "harnitury" if _HEADSET_RE.search(t) else "audio"
    if base_slug == "pobut-tehnika":
        for slug, rx in _APPLIANCE_RE:
            if rx.search(t):
                return slug
        return base_slug
    return base_slug


