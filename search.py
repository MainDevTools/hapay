def _expand(q: str) -> set[str]:
    """Множина варіантів запиту: оригінал + кожна фразова підстановка бренду."""
    base = " ".join(q.strip().lower().split())
    if not base:
        return set()
    out = {base}
    # довші ключі спершу: «ля рош» перед «рош» (якби такий був), щоб не порізати фразу
    for cyr in sorted(_BRANDS, key=len, reverse=True):
        if cyr in base:
            out.add(base.replace(cyr, _BRANDS[cyr]))
    return out


def search_patterns(q: str | None) -> list[str]:
    """ILIKE-патерни для запиту: `['%iphone 15%', '%айфон 15%']`. Порожній запит → []."""
    return [f"%{v}%" for v in sorted(_expand(q or ""))]
